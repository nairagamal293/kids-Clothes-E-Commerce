document.addEventListener("DOMContentLoaded", function () {
    const productsContainer = document.querySelector(".row"); // Target the container for products

    // Fetch products from API
    fetch("http://localhost:5000/api/products") // Adjust API URL
        .then(response => response.json())
        .then(products => {
            productsContainer.innerHTML = ""; // Clear existing content
            
            products.forEach(product => {
                let mainImage = product.Images.find(img => img.IsMainImage) || product.Images[0];
                let price = product.Sizes.length > 0 ? product.Sizes[0].Price : 0; // First size price

                // Generate size buttons dynamically
                let sizeButtons = product.Sizes.map((size, index) => `
                    <label ${index === 0 ? 'class="active"' : ""} for="size-${product.Id}-${size.Size}">
                        <input type="radio" id="size-${product.Id}-${size.Size}" name="size-${product.Id}" value="${size.Size}">
                        ${size.Size}
                    </label>`).join("");

                let productHTML = `
                    <div class="col-lg-4 col-md-6 col-sm-6">
                        <div class="product__item">
                            <div class="product__item__pic set-bg" style="background-image: url('${mainImage.ImagePath}');">
                                <ul class="product__hover">
                                    <li><a href="#"><img src="img/icon/heart.png" alt=""></a></li>
                                    <li><a href="#"><img src="img/icon/compare.png" alt=""> <span>Compare</span></a></li>
                                    <li><a href="#"><img src="img/icon/search.png" alt=""></a></li>
                                </ul>
                            </div>
                            <div class="product__item__text">
                                <h6>${product.Name}</h6>
                                <a href="#" class="add-cart" onclick="addToCart(${product.Id})">+ Add To Cart</a>
                                <h5>$${price.toFixed(2)}</h5>
                                <div class="product__color__select">
                                    ${sizeButtons}
                                </div>
                            </div>
                        </div>
                    </div>
                `;

                productsContainer.innerHTML += productHTML;
            });
        })
        .catch(error => console.error("Error fetching products:", error));
});


function addToCart(productId) {
    let selectedSize = document.querySelector(`input[name="size-${productId}"]:checked`);
    if (!selectedSize) {
        alert("Please select a size.");
        return;
    }

    let size = selectedSize.value;

    fetch("http://localhost:5000/api/cart", { // Adjust API URL
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ productId, size, quantity: 1 }) // Include selected size
    })
    .then(response => response.json())
    .then(data => alert("Product added to cart!"))
    .catch(error => console.error("Error adding to cart:", error));
}
